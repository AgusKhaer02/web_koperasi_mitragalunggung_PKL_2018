<p>
<b>Menimbang:</b><br/>
a. Bahwa berdasarkan Keputusan Menteri Negara Koperasi dan Usaha Kecil Menengah Nomor 123/Kep/M.KUKM/X/2004 tentang Penyelenggaraan Tugas Pembantuan Dalam Rangka Pengesahan Akta Pendirian, Perubahan Anggaran Dasar dan Pembubaran Koperasi pada Provinsi dan Kabupaten/Kota dan untuk lebih maksimal dalam pemberian pelayanan kepada masyarakat, maka dalam pengesahan akta pendirian, perubahan anggaran dasar dan pembubaran koperasi perlu disahkan dengan Keputusan Walikota;<br/>
b. Bahwa berdasarkan pertimbangan sebagaimana tersebut pada huruf a tersebut diatas, maka penetapannya perlu dituangkan dalam Keputusan Walikota Tasikmalaya.<br/></p>
<p>
<b>Mengingat:</b><br/>
1. Undang-Undang Nomor 25 Tahun 1992 tentang Perkoperasian (Lembaran Negara Republik Indonesia Tahun 1992 Nomor 116, Tambahan Lembaran Negara Republik Indonesia Nomor 3502);<br/>
2. Undang-Undang Nommor 10 Tahun 1992 tentang Pembentukan Kota Tasikmalaya (Lembaran Negara Republik Indonesia Tahun 2001 Nomor 86, Tambahan Lembaran Negara Republik Indonesia Nomor 4117);<br/>
3. Undang-Undang Nomor 10 Tahun 2004 tentang Pembentukan Peraturan Perundang-Undangan (Lembaran Negara Republik Indonesia Tahun 2004 Nomor 53, Tambahan Lembaran Negara Republik Indonesia Nomor 4389);<br/>
4. Undang-Undang NOmor 32 Tahun 2004 tentang Pemerintahan Daerah (Lembaran Negara Republik Indonesia Tahun 2004 Nomor 124, Tambahan Lembaran Negara Republik Indonesia Nomor 4437), sebagaimana  telah beberapa kali diubah terakhir dengan Undang-Undang Nomor 12 Tahun 2008 tentang Perubahan Kedua Atas Undang-undang Nomor 32 Tahun 2004 tentang Pemerintahan Daerah (Lembaran Negara Republik Indonesia Tahun 2008 Nomor 59, Tambahan Lembaran Negara Republik Indonesia Nomor 4844);
5. Undang-Undang Nomor 33 Tahun 2004 tentang Penimbangan Keuangan antara Pemerintah Pusat dan Pemerintah Daerah (Lembaran Negara Republik Indonesia Tahun 2004 Nomor 125, Tambahan Lembaran Negara Republik Indonesia Nomer 4438);<br/>
6. Peraturan Pemerintah Nomor 4 Tahun 1994 tentang Persyaratan dan Tata cara Pengesahan Akta Pendirian dan Perubahan Anggaran Dasar Koperasi (Lembaran Negara Republik Indonesia Nomor 3540)<br/>
7. Peraturan Pemerintah Nomor 25 Tahun 2000 tentang Kewenangan Pemerintah dan kewenangan Provinsi sebagai Daerah Otonom (Lembaran Negara Republik Tahun 2000 Nomor 54, Tambahan Lembaran Negara Republik Indonesia Nomor 3540)<br/>
8. Keputusan Meteri Negara Koperasi dan Usaha Kecil Menengah Nomor 98/Kep/M.KUKM/IX/2004 tentang Notaris Sebagai Pembuat Akta Koperasi;<br/>
9. Keputusan Menteri Negara Koperasi dan Usaha Kecil Menengah 123/Kep/M.KUKM/IX/2004 tentang Penyelanggaran Tugas pembatuan Dalam Rangka Pengesahan Akta Pendirian, Perubahan Anggaran Dasar dan Pembuatan koperasi pada Provinsidan Kabupaten/Kota;<br/>
10. Keputusan Menteri Negara Koperasi dan Usaha Kecil Menengah 124/Kep/M.KUKM/IX/2004 tentang Penugasan yang berwenangan untuk Memberikan Pengesahan Akta Pendirian, Perubahan Anggaran Dasar Pembuatan Koperasi di Tingkat Nasional;<br/>
11. Peraturan Daerah Kota Tasikmalaya Nomor 8 Tahun 2008 tentang Pembentukan Organisasi Perangkat Daerah (Lembaran Daerah Kota Tasikmalaya Tahun 2008 Nomor 92) sebagaimana telah diubah dengan Peraturan Daerah Kota Tasikmalaya  Nomor 6 Tahun 2010 tentang Perubahan atas Peraturan Daerah Kota Tasikmalaya tentang Pembentukan Oraganisasi Perangkat Daerah (Lembaran Daerah Kota Tasikmalaya Tahun 2010 Nomor 119); <br/>
12. Peraturan Walikota Tasikmalaya Nomor 25 Tahun 2008 tentang Petugas Pokok  Fungsi dan Rincian Tugas Unit Dinas Koperasi Usaha mikro Kecil Menengah Perindustrian dan Perdagangan (Berita Daerah Kota Tasikmalaya Tahun 2008 Nomor 209A).<br/></p>
